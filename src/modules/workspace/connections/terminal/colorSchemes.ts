export function resolveTerminalColorScheme(schemeId: string | null | undefined): TerminalColorScheme {
  if (!schemeId) {
    return KKTERM_SCHEME;
  }
  return TERMINAL_COLOR_SCHEMES.find((scheme) => scheme.id === schemeId) ?? KKTERM_SCHEME;
}

/** `#rrggbb` → `rgba(r, g, b, alpha)`. Falls back to the input on malformed hex. */
export function hexColorWithAlpha(hex: string, alpha: number) {
  const normalized = hex.trim();
  const match = /^#([0-9a-fA-F]{6})$/.exec(normalized);
  if (!match) {
    return normalized;
  }
  const value = Number.parseInt(match[1], 16);
  const clampedAlpha = Math.min(Math.max(alpha, 0), 1);
  const r = (value >> 16) & 0xff;
  const g = (value >> 8) & 0xff;
  const b = value & 0xff;
  return `rgba(${r}, ${g}, ${b}, ${clampedAlpha})`;
}
